G4VPhysicalVolume* B1DetectorConstruction::Construct()
{  
  // Get nist material manager
  G4NistManager* nist = G4NistManager::Instance();
  G4double env_sizeXY = 20*cm, env_sizeZ = 20*cm;
  G4bool checkOverlaps = true;

  // World
  G4double world_sizeXY = 1.2*env_sizeXY;
  G4double world_sizeZ  = 1.2*env_sizeZ;
  G4Material* world_mat = nist->FindOrBuildMaterial("G4_Galactic");
  
  G4Box* solidWorld =    
    new G4Box("World",                       //its name
       0.5*world_sizeXY, 0.5*world_sizeXY, 0.5*world_sizeZ);     //its size
      
  G4LogicalVolume* logicWorld =                         
    new G4LogicalVolume(solidWorld,          //its solid
                        world_mat,           //its material
                        "World");            //its name
                                   
  G4VPhysicalVolume* physWorld = 
    new G4PVPlacement(0,                     //no rotation
                      G4ThreeVector(),       //at (0,0,0)
                      logicWorld,            //its logical volume
                      "World",               //its name
                      0,                     //its mother  volume
                      false,                 //no boolean operation
                      0,                     //copy number
                      checkOverlaps);        //overlaps checking
 
  G4Material* shape1_mat = nist->FindOrBuildMaterial("G4_Pb");
  G4Box* solid1 = new G4Box("solid1",                    //its name
			    0.5*2*cm, 0.5*2*cm, 0.6*mm); //its size
  G4LogicalVolume* logic1 =
    new G4LogicalVolume(solid1,            //its solid
                        shape1_mat,             //its material
                        "logic1");         //its name

  new G4PVPlacement(0,                       //no rotation
                    G4ThreeVector(0,0,0),
                    logic1,                //its logical volume
                    "target",              //its name
                    logicWorld,              //its mother  volume
                    false,                   //no boolean operation
                    0,                       //copy number
                    checkOverlaps);          //overlaps checking

  return physWorld;
}

